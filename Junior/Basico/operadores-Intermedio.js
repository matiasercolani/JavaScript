//Operadores de comparacion
let numero = 23;
let numero2 = 13;
let texto = '23';



//Igualdad (no diferencia entre texto y numero)
document.write(numero == texto);

//Estrictamente Igual (si diferencia entre texto y numero)
document.write(texto===numero);

//Desigual
document.write(numero != numero2);


//Estrictamente Desigual (si diferencia entre texto y numero)
document.write(texto !== numero);


//Mayor que, menor que, Mayor o igual que, Menor o igual que
document.write(texto > numero2);
document.write(texto < numero2);

document.write(texto >= numero2);
document.write(texto <= numero2);


// -----------------------------
// Operadores logicos
let uno = true;
let dos = true;

// Y
document.write(uno && dos);
// O
document.write(uno || dos);
//Negacion
document.write(!uno);




